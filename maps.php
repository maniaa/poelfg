	return $maps;
}

function enrichMaps() {
	$maps = getMaps();
	foreach($maps as $idMap => $map) {
		$maps[$idMap]['image'] = preg_replace('/ /', '_', $map['name']);
		$maps[$idMap]['image'] = preg_replace('/\'/', '', $maps[$idMap]['image']);
		$maps[$idMap]['image'] = $maps[$idMap]['image'] . '.png';
	}
	
	var_export($maps);
}